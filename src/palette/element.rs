//! Defines elements, which encapsulates the component of a Palette that is
//! needed to provide a single color.
use color::Color;

use std::cell::{RefCell, Cell};
use std::ops::{Deref, DerefMut};
use std::rc::Rc;
use std::fmt;


/// A 'slot' in the `Palette` in which to insert colors and related data. 
/// Enables tree structured arrangements of `ColorElement`s.
pub type PaletteSlot = Rc<PaletteElement>;


/// A wrapper around a `ColorElement` for tracking its status and enabling 
/// interior mutability.
#[derive(Debug)]
pub struct PaletteElement {
	/// The `ColorElement` being wrapped.
	color_element: RefCell<ColorElement>,
	/// The status of the `ColorElement`. An invalid status means that the 
	/// `ColorElement` refers to something that is also invalid or not in the 
	/// `Palette`.
	valid: Cell<bool>,
}


impl PaletteElement {
	/// Creates a new `PaletteElement` wrapping the given `ColorElement`.
	pub fn new(element: ColorElement) -> Self {
		PaletteElement {
			color_element: RefCell::new(element),
			valid: Cell::new(false),
		}
	}

	/// Returns the `Color` of the internal `ColorElement`, or `None` if it is 
	/// invalid.
	pub fn get_color(&self) -> Option<Color> {
		if self.is_valid() {
			Some(self.color_element.borrow().get_color())
		} else {
			None
		}
	}

	/// Makes the internal `ColorElement` valid. This will only persist if all 
	/// of this `ColorElement`'s referants are also valid and present in the 
	/// `Palette`.
	pub fn restore(&mut self) {
		self.valid.set(true);
	}

	/// Makes the internal `ColorElement` invalid. This will always persist, but
	/// it will not remove the `PaletteSlot` from the `Palette`.
	pub fn invalidate(&mut self) {
		self.valid.set(false);
	}

	/// Returns whether the internal `ColorElement` is valid. This function will
	/// check on and update the status of any of its referants. 
	pub fn is_valid(&self) -> bool {
		match &*self.color_element.borrow() {
			&ColorElement::FirstOrder {ref parent, ..} => {
					self.valid.set(parent.is_valid());
				},

			&ColorElement::SecondOrder {ref parents, ..} => {
					self.valid.set(parents.0.is_valid() && parents.1.is_valid());
				},
			_ => ()
		};
		self.valid.get()
	}
}


impl Deref for PaletteElement {
	type Target = RefCell<ColorElement>;
	fn deref(&self) -> &Self::Target {
		&self.color_element
	}
}


impl DerefMut for PaletteElement {
	fn deref_mut(&mut self) -> &mut Self::Target {
		&mut self.color_element
	}
}


/// Encapsulates a `Palette` component for generating a single `Color`.
pub enum ColorElement {
	/// An element with no dependencies. Generates the given `Color`.
	ZerothOrder {
		/// The color to be generated by the ColorElement.
		color: Color
	},

	/// An element with a single dependency. Generates a color from another.
	FirstOrder {
		/// The function that builds the color from its dependencies.
		build: Box<Fn(&ColorElement) -> Color>,
		/// The PaletteSlot to use for building a color.
		parent: PaletteSlot,
	},

	/// An element with two dependencies. Generates a color from two others.
	SecondOrder {
		/// The function that builds the color from its dependencies.
		build: Box<Fn(&ColorElement, &ColorElement) -> Color>,
		/// The PaletteSlots to use for building a color.
		parents: (PaletteSlot, PaletteSlot),
	},
}


impl ColorElement {
	/// Returns the Color generated by the ColorElement.
	pub fn get_color(&self) -> Color {
		match *self {
			ColorElement::ZerothOrder {color} 
				=> color,

			ColorElement::FirstOrder {ref build, ref parent} 
				=> build(&*parent.borrow()),

			ColorElement::SecondOrder {ref build, ref parents}
				=> build(&*parents.0.borrow(), &*parents.1.borrow())
		}
	}

	/// Returns the element's dependency order.
	pub fn get_order(&self) -> u8 {
		match self {
			&ColorElement::ZerothOrder {..} => 0,
			&ColorElement::FirstOrder {..} => 1,
			&ColorElement::SecondOrder {..} => 2
		}
	}
}


impl fmt::Debug for ColorElement {
	fn fmt(&self, f: &mut fmt::Formatter) -> Result<(), fmt::Error> {
		match *self {
			ColorElement::ZerothOrder {ref color} 
				=> write!(f, 
					"ColorElement::ZerothOrder {{ color: {:?} }}", 
					color),

			ColorElement::FirstOrder {ref parent, ..} 
				=> write!(f, 
					"ColorElement::FirstOrder {{ parent: {:?}, build: {:?} }}", 
					parent, 
					self.get_color()),

			ColorElement::SecondOrder {ref parents, ..} 	
				=> write!(f, 
					"ColorElement::SecondOrder {{ parents: {:?}, build: {:?} }}", 
					parents, 
					self.get_color()),
		}
	}
}

impl Default for ColorElement {
	fn default() -> Self {
		ColorElement::ZerothOrder {color: Default::default()}
	}
}